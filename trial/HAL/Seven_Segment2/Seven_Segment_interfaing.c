void SEG2_Initialization()
{
	DIO_SETPIN_DIRECTION(SEG_PRT,DIO_PIN0,DIO_OUTPUT);
	DIO_SETPIN_DIRECTION(SEG_PRT,DIO_PIN1,DIO_OUTPUT);
	DIO_SETPIN_DIRECTION(SEG_PRT,DIO_PIN2,DIO_OUTPUT);
	DIO_SETPIN_DIRECTION(SEG_PRT,DIO_PIN3,DIO_OUTPUT);
	DIO_SETPIN_DIRECTION(SEG_PRT,DIO_PIN4,DIO_OUTPUT);
	DIO_SETPIN_DIRECTION(SEG_PRT,DIO_PIN5,DIO_OUTPUT);
	DIO_SETPIN_DIRECTION(SEG_PRT,DIO_PIN6,DIO_OUTPUT);
}
void SEG2_Dispaly(uint16_t num)
{
	Uint8_t seg_data[] = {0b0111111,0b0110000,0b01011011,0b01001111,0b01100110,0b01101101,0b01111101,0b00000111,0b01111111,0b01101111,0b01110111};

PORTA = seg_data[num];	
}

void SEG2_DisplayChar(uint16_t let)
{
	PORTA = let ;
}
void SEG_Stop()
{
	DIO_SETPIN_VALUE(SEG_PRT,DIO_PIN0,DIO_LOW);
	DIO_SETPIN_VALUE(SEG_PRT,DIO_PIN1,DIO_LOW);
	DIO_SETPIN_VALUE(SEG_PRT,DIO_PIN2,DIO_LOW);
	DIO_SETPIN_VALUE(SEG_PRT,DIO_PIN3,DIO_LOW);
	DIO_SETPIN_VALUE(SEG_PRT,DIO_PIN4,DIO_LOW);
	DIO_SETPIN_VALUE(SEG_PRT,DIO_PIN5,DIO_LOW);
	DIO_SETPIN_VALUE(SEG_PRT,DIO_PIN6,DIO_LOW);
}
